    private void jButtonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCariActionPerformed
        // TODO add your handling code here:
        try{
            teller_ktp = Integer.parseInt(jTextFieldCari.getText());
            Connection koneksi = KoneksiDB.getKoneksi();
            Statement s = koneksi.createStatement();
            String sql = "SELECT * FROM teller WHERE teller_ktp ="+this.teller_ktp;
            ResultSet rs = s.executeQuery(sql);
            while(rs.next()){
                teller_nama = rs.getString("teller_nama");
                teller_username = rs.getString("teller_username");
                teller_password = rs.getString("teller_password");
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Teller tidak ditemukan");
        }
        
        jTextFieldKtp.setText(Integer.toString(teller_ktp));
        jTextFieldNama.setText(teller_nama);
        jTextFieldUsername.setText(teller_username);
        jTextFieldPassword.setText(teller_password);
        jTextFieldConfirm.setText("");
        
        jButtonCreate.setEnabled(false);
        jButtonEdit.setEnabled(true);
        jButtonDelete.setEnabled(true);
        jButtonCancel.setEnabled(true);
        
    }//GEN-LAST:event_jButtonCariActionPerformed

    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        // TODO add your handling code here:
        if(jTextFieldNama.getText().equals("") || jTextFieldKtp.getText().equals("") || jTextFieldUsername.getText().equals("") || jTextFieldPassword.getText().equals("") || jTextFieldConfirm.getText().equals("") ){
            JOptionPane.showMessageDialog(this, "Data teller masing kosong");
        }else{
            if(jTextFieldPassword.getText().equals(jTextFieldConfirm.getText())){
                try{
                teller_ktp = Integer.parseInt(jTextFieldKtp.getText());
                teller_nama = jTextFieldNama.getText();
                teller_username = jTextFieldUsername.getText();
                teller_password = jTextFieldPassword.getText();

                Connection koneksi = KoneksiDB.getKoneksi();
                Statement s = koneksi.createStatement();
                String sql = "INSERT INTO teller(teller_ktp, teller_nama, teller_username, teller_password) VALUES (?, ?, ?, ?)";
                PreparedStatement p = koneksi.prepareStatement(sql);     

                p.setInt(1, teller_ktp);
                p.setString(2, teller_nama);
                p.setString(3, teller_username);
                p.setString(4, teller_password);

                p.executeUpdate();
                p.close();

                JOptionPane.showMessageDialog(null, "Akun Teller berhasil dibuat");

                }catch(SQLException e){
                    System.out.println(e);
                    System.out.println("error");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Password doesn't match");
            }
        }
    }//GEN-LAST:event_jButtonCreateActionPerformed

