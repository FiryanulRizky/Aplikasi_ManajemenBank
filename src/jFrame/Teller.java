    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
        if(jTextFieldPassword.getText().equals(jTextFieldConfirm.getText())){
            try{
                teller_ktp = Integer.parseInt(jTextFieldKtp.getText());
                teller_nama = jTextFieldNama.getText();
                teller_username = jTextFieldUsername.getText();
                teller_password = jTextFieldPassword.getText();

                Connection koneksi = KoneksiDB.getKoneksi();
                Statement s = koneksi.createStatement();
                String sql = "UPDATE teller SET teller_nama = ?, teller_username = ?, teller_password = ? WHERE teller_ktp = ?";
                PreparedStatement p = koneksi.prepareStatement(sql);     

                p.setString(1, teller_nama);
                p.setString(2, teller_username);
                p.setString(3, teller_password);
                p.setInt(4, teller_ktp);

                p.executeUpdate();
                p.close();

                JOptionPane.showMessageDialog(null, "Akun Teller berhasil diubah");
                
                jButtonCreate.setEnabled(true);
                jButtonCancel.setEnabled(false);
                jButtonDelete.setEnabled(false);
                jButtonEdit.setEnabled(false);
                
                
            }catch(SQLException e){
                System.out.println(e);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Password doesn't match");
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int hapus = JOptionPane.showOptionDialog(this, 
                    "Yakin ingin menghapus data teller?", 
                    "Hapus Data Teller", 
                    JOptionPane.YES_NO_OPTION, 
                    JOptionPane.QUESTION_MESSAGE, null, null, null);
    
        if(hapus == JOptionPane.YES_OPTION){
            try{

                Connection koneksi = KoneksiDB.getKoneksi();
                Statement s = koneksi.createStatement();
                String sql = "DELETE FROM teller WHERE teller_ktp = ?";
                PreparedStatement p = koneksi.prepareStatement(sql);     
                p.setInt(1, teller_ktp);
                p.executeUpdate();
                p.close();

                JOptionPane.showMessageDialog(null, "Akun Teller berhasil dihapus");
                
                jTextFieldCari.setText("");
                jTextFieldKtp.setText("");
                jTextFieldNama.setText("");
                jTextFieldUsername.setText("");
                jTextFieldPassword.setText("");
                
                jButtonCreate.setEnabled(true);
                jButtonCancel.setEnabled(false);
                jButtonDelete.setEnabled(false);
                jButtonEdit.setEnabled(false);
                
            }catch(SQLException e){
                    System.out.println(e);
                    System.out.println("error");
            }
        }
        
    }//GEN-LAST:event_jButtonDeleteActionPerformed

