    private void jButtonDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDaftarActionPerformed
        // TODO add your handling code here:
        ValidasiForm validasi = new ValidasiForm(jTextFieldJenis.getText(), 
                jTextFieldKtp.getText(), jTextFieldRekening.getText(),
                jTextFieldNama.getText(), jDateChooserTTL.getDate(), 
                jTextFieldAlamat.getText(), Integer.parseInt(jTextFieldSaldo.getText()));
        
        if(validasi.cekFormBiasa()){
            daftarGagal();
        }else{
            validasi.setInputNasabahBaru();
            int jmlDebit = Integer.parseInt(jTextFieldSaldo.getText());
            TransaksiRek transDeb = new TransaksiRek();
            transDeb.setTransaksi(Integer.parseInt(noRek));
            transDeb.setDebit(jmlDebit, teller_ktp);
            daftarBerhasil();
        }
    }//GEN-LAST:event_jButtonDaftarActionPerformed

